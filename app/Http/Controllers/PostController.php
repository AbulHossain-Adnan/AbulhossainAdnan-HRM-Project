     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'name'=>'required',
            'user_id'=>'required',
            'details'=>'required',
        ]);
        Post::insert([
            'name'=>$request->name,
            'user_id'=>$request->user_id,
            'details'=>$request->details,
        ]);
        return redirect()->route('post.create');
    }

